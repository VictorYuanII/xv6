	.text

	/*
         * save the old thread's registers,
         * restore the new thread's registers.
         */

	.globl thread_switch
thread_switch:
	/* YOUR CODE HERE */
	sd ra, 0(a0)
	sd sp, 8(a0)
	sd s0, 16(a0)
	sd s1, 24(a0)
	sd s2, 32(a0)
	sd s3, 40(a0)
	sd s4, 48(a0)
	sd s5, 56(a0)
	sd s6, 64(a0)
	sd s7, 72(a0)
	sd s8, 80(a0)
	sd s9, 88(a0)
	sd s10, 96(a0)
	sd s11, 104(a0)
/*  Store Double，用于将一个双字（64位数据）存储到内存中。它将指定寄存器中的数据存储到内存的指定地址。 */
/*ld 表示 Load Double，用于从内存中加载一个双字（64位数据）到寄存器中。它从内存的指定地址加载数据并将其放入指定寄存器。*/
/*其中，a0 为当前线程的 context 结构地址，a1 为被调度线程的context 结构地址。*/
/*只需保存 callee-saved 寄存器，以及 返回地址 ra、栈指针 sp 即可*/
	ld ra, 0(a1)
	ld sp, 8(a1)
	ld s0, 16(a1)
	ld s1, 24(a1)
	ld s2, 32(a1)
	ld s3, 40(a1)
	ld s4, 48(a1)
	ld s5, 56(a1)
	ld s6, 64(a1)
	ld s7, 72(a1)
	ld s8, 80(a1)
	ld s9, 88(a1)
	ld s10, 96(a1)
	ld s11, 104(a1)

	ret    /* return to ra */
